# Create a list with different data types
mixed_list = ["Hello", 42, 3.14, True]

# Extract and print each element separately
string_element = mixed_list[0]  # Extracting the string
integer_element = mixed_list[1]  # Extracting the integer
float_element = mixed_list[2]    # Extracting the float
boolean_element = mixed_list[3]   # Extracting the boolean

# Printing each element
print("String Element:", string_element)
print("Integer Element:", integer_element)
print("Float Element:", float_element)
print("Boolean Element:", boolean_element)

