def add_numbers(a, b):
    """
    Takes two numbers and returns their sum.
    
    Parameters:
        a (int or float): The first number.
        b (int or float): The second number.
        
    Returns:
        int or float: The sum of the two numbers.
    """
    return a + b

# Example usage
result = add_numbers(5, 7)
print("The sum is:", result)
