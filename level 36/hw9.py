def square_number(number):
    """
    Returns the square of the given number.
    
    Parameters:
        number (int or float): The number to square.
        
    Returns:
        int or float: The square of the number.
    """
    return number ** 2

# Example usage
num = 4
result = square_number(num)
print(f"The square of {num} is {result}.")
