def print_sum():
    # Accept the first number as input
    num1 = float(input("Enter the first number: "))
    
    # Accept the second number as input
    num2 = float(input("Enter the second number: "))
    
    # Calculate their sum
    total = num1 + num2
    
    # Print the result
    print("The sum of the two numbers is:", total)

# Call the function
print_sum()
